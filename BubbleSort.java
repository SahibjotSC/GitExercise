public class BubbleSort extends Sort
{
    public BubbleSort(int[] array)
    {
        super(array);
    }
    
    public void SortArray()
    {
        for (int i = 0; i < array.length - 1; i++) {
            for (int j = 0; j < array.length - 1 - i; j++) {
                if (array[j] > array[j + 1]) {
                    int temp = array[j];
                    array[j] = array[j + 1];
                    array[j + 1] = temp;
                }
            }
        }
    }
}